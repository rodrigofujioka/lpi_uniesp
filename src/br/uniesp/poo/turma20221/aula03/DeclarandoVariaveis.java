package br.uniesp.poo.turma20221.aula03;

public class DeclarandoVariaveis {

    public static void main(String[] args) {

        int x = 7;

        while(x > 3){
            System.out.println(x);
            x = x - 1;
        }

        while(x < 7){
            System.out.println(x);
            x = x + 1;
        }
    }
}
